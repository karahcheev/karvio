        suite = all_suites[(case_index - 1) % len(all_suites)]
        product = bundle.products[(case_index - 1) % len(bundle.products)]
        owner = users[case_index % len(users)]
        priority = rng.choices(
            [
                TestCasePriority.low,
                TestCasePriority.medium,
                TestCasePriority.high,
                TestCasePriority.critical,
            ],
            weights=[10, 45, 30, 15],
            k=1,
        )[0]
        is_automated = case_index % 4 == 0
        verb = rng.choice(CASE_VERBS)
        obj = rng.choice(CASE_OBJECTS)
        title = f"{suite.name}: user {verb} {obj}"
        case_key = f"DEMO-{config.slug.upper()}-{bundle.project.id[-1]}-{case_index:04d}"
        template_payload = {
            "source": "demo-generator",
            "risk": priority.value,
            "business_flow": suite.name,
        }
        if is_automated:
            template_payload.update(
                {
                    "raw_test": (
                        f"def test_demo_case_{case_index:04d}(api_client):\n"
                        f"    response = api_client.get('/demo/{case_key}')\n"
                        "    assert response.status_code in {200, 204}"
                    ),
                    "raw_test_language": "python",
                }
            )
