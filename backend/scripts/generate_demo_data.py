async def amain() -> None:
    config = _parse_args()
    async with AsyncSessionLocal() as db:
        try:
            summary = await generate_demo_data(db, config)
            await db.commit()
        except Exception:
            await db.rollback()
            raise
    _print_summary(summary, config.user_password)


def main() -> None:
    asyncio.run(amain())


if __name__ == "__main__":
    main()
