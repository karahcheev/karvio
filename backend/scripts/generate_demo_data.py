async def generate_demo_data(db: AsyncSession, config: GeneratorConfig) -> DemoSummary:
    rng = random.Random(config.seed)
    if config.reset:
        await reset_demo_data(db)

    users = await _create_users(db, config)
    bundles: list[ProjectBundle] = []

    for index in range(config.projects):
        template = PROJECT_TEMPLATES[index % len(PROJECT_TEMPLATES)]
        bundle = _create_project_shell(template, index, config)
        db.add(bundle.project)
        await db.flush()

        _add_memberships(db, bundle.project, users)
        _add_suites(db, bundle, template)
        _add_milestones(db, bundle, users, index)
        _add_products_and_components(db, bundle, template, users, rng)
        _add_environments(db, bundle, users, index)
        _add_datasets(db, bundle, users, config, rng)
        await db.flush()

        _add_test_cases(db, bundle, users, config, rng)
        _add_test_case_evidence(db, bundle, users, config, rng)
        await db.flush()

        _add_test_plans(db, bundle, users, config, rng)
        _add_test_runs(db, bundle, users, config, rng)
        _add_performance_runs(db, bundle, users, config, rng)
        bundles.append(bundle)

    await db.flush()
    return _build_summary(config.slug, users, bundles)


