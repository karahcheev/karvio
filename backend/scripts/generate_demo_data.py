def _utc_now() -> datetime:
    return datetime.now(timezone.utc)


def _date_offset(days: int) -> date:
    return (_utc_now() + timedelta(days=days)).date()


def _dt_offset(days: int, minutes: int = 0) -> datetime:
    return _utc_now() + timedelta(days=days, minutes=minutes)


def _hash_snapshot(value: dict[str, Any]) -> str:
    payload = repr(sorted(value.items())).encode("utf-8")
    return hashlib.sha256(payload).hexdigest()


def _pick_weighted_status(rng: random.Random) -> RunItemStatus:
    return rng.choices(
        [
            RunItemStatus.passed,
            RunItemStatus.failure,
            RunItemStatus.blocked,
            RunItemStatus.skipped,
            RunItemStatus.untested,
            RunItemStatus.error,
        ],
        weights=[58, 14, 7, 8, 9, 4],
        k=1,
    )[0]


def _status_counts(statuses: Sequence[RunItemStatus]) -> dict[str, int]:
    result = {status.value: 0 for status in RunItemStatus}
    for status in statuses:
        result[status.value] += 1
    return result


def _status_for_run(item_statuses: Sequence[RunItemStatus], *, is_latest_run: bool) -> TestRunStatus:
    if is_latest_run:
        return TestRunStatus.in_progress
    if all(status == RunItemStatus.untested for status in item_statuses):
        return TestRunStatus.not_started
    return TestRunStatus.completed


def _run_case_rollup(row_statuses: Sequence[RunItemStatus]) -> RunItemStatus:
    if not row_statuses:
        return RunItemStatus.untested
    if any(status in {RunItemStatus.failure, RunItemStatus.error} for status in row_statuses):
        return RunItemStatus.failure
    if any(status == RunItemStatus.blocked for status in row_statuses):
        return RunItemStatus.blocked
    if all(status == RunItemStatus.passed for status in row_statuses):
        return RunItemStatus.passed
    if all(status == RunItemStatus.skipped for status in row_statuses):
        return RunItemStatus.skipped
    return RunItemStatus.in_progress


async def reset_demo_data(db: AsyncSession) -> None:
    project_ids = list(
        await db.scalars(
            select(Project.id).where(
                Project.description.is_not(None),
                Project.description.contains(DEMO_MARKER),
            )
        )
    )
