def _add_test_plans(
    db: AsyncSession,
    bundle: ProjectBundle,
    users: Sequence[User],
    config: GeneratorConfig,
    rng: random.Random,
) -> None:
    plan_specs = [
        ("Pre-deploy smoke", ["smoke", "deploy", "release"], TestPlanGenerationSource.manual, "smoke"),
        ("Release regression", ["regression", "release"], TestPlanGenerationSource.product_generated, "regression"),
        ("Critical path acceptance", ["critical-path", "release"], TestPlanGenerationSource.manual, "critical"),
        ("API contract sweep", ["api", "integration"], TestPlanGenerationSource.manual, "api"),
        ("Security and access review", ["security", "negative"], TestPlanGenerationSource.manual, "security"),
        ("Customer-facing exploratory charter", ["ui", "exploratory"], TestPlanGenerationSource.manual, "ui"),
        ("Cross-platform mobile smoke", ["mobile", "smoke"], TestPlanGenerationSource.manual, "mobile"),
        ("Integration dependency regression", ["integration", "regression"], TestPlanGenerationSource.product_generated, "dependency"),
        ("Post-deploy monitoring checks", ["smoke", "production-like"], TestPlanGenerationSource.manual, "post-deploy"),
    ]
    active_cases = [case for case in bundle.cases if case.status == TestCaseStatus.active]
    for plan_index, (name, tags, source, focus) in enumerate(plan_specs, start=1):
        selected_suites = rng.sample(bundle.suites, k=min(rng.randint(1, 3), len(bundle.suites)))
        product = bundle.products[(plan_index - 1) % len(bundle.products)]
        if focus == "critical":
            candidates = [
                case
                for case in active_cases
                if case.priority in {TestCasePriority.high, TestCasePriority.critical}
            ]
        elif focus in {"api", "security", "mobile", "ui"}:
            candidates = [case for case in active_cases if focus in case.tags]
        elif source == TestPlanGenerationSource.product_generated:
            candidates = [case for case in active_cases if case.primary_product_id == product.id]
        else:
            candidates = [case for case in active_cases if set(tags) & set(case.tags)]
        if len(candidates) < 8:
            candidates = active_cases
        target_case_count = min(len(candidates), rng.randint(10, max(14, min(32, len(candidates)))))
        selected_cases = rng.sample(candidates, k=target_case_count)
        generation_config = {
            "product_ids": [product.id] if source == TestPlanGenerationSource.product_generated else [],
            "component_ids": [],
            "include_dependent_components": source == TestPlanGenerationSource.product_generated,
            "minimum_risk_level": "high" if focus in {"critical", "dependency"} else None,
            "generation_mode": "smoke" if "smoke" in tags else "regression",
            "explicit_include_case_ids": [],
            "explicit_exclude_case_ids": [],
        }
        plan = TestPlan(
            id=generate_id(),
            project_id=bundle.project.id,
            name=f"{name} {config.slug}-{bundle.project.id[-1]}",
            description=(
                f"{DEMO_MARKER} Realistic {focus} plan used by QA leads to start repeatable runs "
                "without rebuilding scope manually."
            ),
            tags=tags,
            generation_source=source,
            generation_config=generation_config,
            generation_summary={
                "suite_count": len(selected_suites),
                "case_count": len(selected_cases),
                "product_id": product.id if source == TestPlanGenerationSource.product_generated else None,
                "mandatory_count": sum(1 for case in selected_cases if case.priority == TestCasePriority.critical),
                "automated_count": sum(1 for case in selected_cases if case.test_case_type == TestCaseType.automated),
            },
            created_by=rng.choice(users).id,
            milestone_id=bundle.milestones[plan_index % len(bundle.milestones)].id,
            created_at=_dt_offset(-22 + plan_index * 2),
            updated_at=_dt_offset(-(plan_index % 5)),
        )
        db.add(plan)
        bundle.plans.append(plan)
        bundle.plan_case_ids_by_plan[plan.id] = [case.id for case in selected_cases]
        for suite in selected_suites:
            db.add(TestPlanSuite(id=generate_id(), test_plan_id=plan.id, suite_id=suite.id))
        for case in selected_cases:
            db.add(TestPlanCase(id=generate_id(), test_plan_id=plan.id, test_case_id=case.id))


def _add_test_runs(
    db: AsyncSession,
    bundle: ProjectBundle,
    users: Sequence[User],
    config: GeneratorConfig,
    rng: random.Random,
) -> None:
    script_now = _utc_now().replace(microsecond=0)
    run_days = max(1, config.runs_per_project)
    first_day = script_now.date() - timedelta(days=run_days - 1)
    scheduled_starts: list[datetime] = []
    for day_offset in range(run_days):
        run_date = first_day + timedelta(days=day_offset)
        runs_for_day = rng.randint(1, 5)
        day_start = datetime(run_date.year, run_date.month, run_date.day, tzinfo=timezone.utc)
        if run_date == script_now.date():
            elapsed_minutes = max(0, int((script_now - day_start).total_seconds() // 60))
            latest_start = script_now - timedelta(hours=3) if elapsed_minutes >= 240 else script_now
            available_minutes = max(1, int((latest_start - day_start).total_seconds() // 60) - 15)
            earlier_count = max(0, runs_for_day - 1)
            earlier_minutes = sorted(rng.randint(15, max(15, available_minutes)) for _ in range(earlier_count))
            scheduled_starts.extend(day_start + timedelta(minutes=minute) for minute in earlier_minutes)
            scheduled_starts.append(latest_start)
        else:
            workday_minutes = sorted(rng.sample(range(8 * 60, 19 * 60), k=runs_for_day))
            scheduled_starts.extend(day_start + timedelta(minutes=minute) for minute in workday_minutes)

    creator_weights = [5, 8, 7, 3, 2, 4]
    run_kinds = [
        ("Smoke", "smoke"),
        ("Regression", "regression"),
        ("Release acceptance", "release"),
        ("Defect retest", "retest"),
        ("Exploratory charter", "exploratory"),
    ]

    for run_index, started_at in enumerate(sorted(scheduled_starts), start=1):
        is_latest_run = run_index == len(scheduled_starts)
        env = rng.choice(bundle.environments)
        env_revisions = bundle.environment_revisions_by_environment.get(env.id, [])
        available_revisions = [revision for revision in env_revisions if revision.created_at <= started_at]
        env_revision = max(available_revisions or env_revisions, key=lambda revision: revision.revision_number)
        milestone = rng.choices(
            bundle.milestones,
            weights=[5 if milestone.status == MilestoneStatus.active else 3 if milestone.status == MilestoneStatus.planned else 1 for milestone in bundle.milestones],
            k=1,
        )[0]
        eligible_cases = [case for case in bundle.cases if case.status == TestCaseStatus.active]
        case_count = min(max(12, len(eligible_cases) // 2), len(eligible_cases))
        selected_plan = rng.choice(bundle.plans) if bundle.plans and rng.random() < 0.65 else None
        if selected_plan is not None:
            plan_case_ids = set(bundle.plan_case_ids_by_plan.get(selected_plan.id, []))
            plan_cases = [case for case in bundle.cases if case.id in plan_case_ids and case.status == TestCaseStatus.active]
        else:
            plan_cases = []
        selected_cases = (
            rng.sample(plan_cases, k=min(len(plan_cases), case_count))
            if plan_cases
            else rng.sample(eligible_cases, k=case_count)
        )
        creator = rng.choices(users, weights=creator_weights, k=1)[0]
        assignee = rng.choice(users)
        run_label, run_tag = rng.choice(run_kinds)
        build_channel = "rc" if milestone.status in {MilestoneStatus.active, MilestoneStatus.planned} else "build"
        test_run = TestRun(
            id=generate_id(),
            project_id=bundle.project.id,
            name=f"{run_label} {started_at.strftime('%Y-%m-%d')} {config.slug}-{bundle.project.id[-1]}-{run_index:03d}",
            description=(
                f"{DEMO_MARKER} {run_tag.title()} run generated for daily release-readiness history. "
                f"Source plan: {selected_plan.name if selected_plan else 'ad hoc scope'}."
            ),
            environment_id=env.id,
            environment_revision_id=env_revision.id,
            environment_revision_number=env_revision.revision_number,
            environment_name_snapshot=env.name,
            environment_snapshot=env_revision.full_snapshot,
            milestone_id=milestone.id,
            build=f"{build_channel}-{config.slug}-{started_at.strftime('%Y%m%d')}-{run_index:03d}",
            assignee=assignee.id,
            status=TestRunStatus.in_progress,
            planned_item_count=len(selected_cases),
            created_by=creator.id,
            started_at=started_at,
            completed_at=None,
            created_at=started_at - timedelta(minutes=rng.randint(20, 180)),
            updated_at=script_now if is_latest_run else started_at,
        )
        db.add(test_run)
        bundle.runs.append(test_run)

        item_statuses: list[RunItemStatus] = []
        latest_finished_at = started_at
        for item_index, case in enumerate(selected_cases, start=1):
            dataset = bundle.datasets[item_index % len(bundle.datasets)] if item_index % 4 == 0 else None
            rows = bundle.dataset_rows_by_dataset.get(dataset.id, [])[:3] if dataset else []
            row_statuses = [_pick_weighted_status(rng) for _ in rows]
            status = _run_case_rollup(row_statuses) if rows else _pick_weighted_status(rng)
            if is_latest_run:
                if item_index % 7 == 0:
                    status = RunItemStatus.untested
                elif item_index % 5 == 0:
                    status = RunItemStatus.in_progress
            started = started_at + timedelta(minutes=item_index * 3)
            finished = started + timedelta(minutes=rng.randint(2, 20))
            duration_ms = int((finished - started).total_seconds() * 1000)
            if is_latest_run and started > script_now:
                status = RunItemStatus.untested
                row_statuses = [RunItemStatus.untested for _ in rows]
            elif is_latest_run and finished > script_now:
                status = RunItemStatus.in_progress
            defects = [f"BUG-{rng.randint(1200, 9999)}"] if status in {RunItemStatus.failure, RunItemStatus.error} else []
            is_open_item = status == RunItemStatus.in_progress
            executed_by = rng.choices(users, weights=creator_weights, k=1)[0]
            item_assignee = rng.choice(users)
            item = RunItem(
                id=generate_id(),
                test_run_id=test_run.id,
                test_case_id=case.id,
                dataset_id=dataset.id if dataset else None,
                dataset_snapshot=_dataset_snapshot(dataset, rows) if dataset else {},
                test_case_key_snapshot=case.key,
                test_case_title_snapshot=case.title,
                test_case_priority_snapshot=case.priority,
                test_case_tags_snapshot=case.tags,
                suite_name_snapshot=next((suite.name for suite in bundle.child_suites if suite.id == case.suite_id), None),
                assignee_id=item_assignee.id,
                time=case.time,
                comment=f"{DEMO_MARKER} Result from manual QA session.",
                defect_ids=defects,
                actual_result=_actual_result(status),
                system_out="Generated smoke log: request id demo-req-001",
                system_err="Validation failed on edge case" if defects else None,
                executed_by=executed_by.id,
                status=status,
                rows_total=len(rows),
                rows_passed=sum(1 for row_status in row_statuses if row_status == RunItemStatus.passed),
                rows_failed=sum(
                    1 for row_status in row_statuses if row_status in {RunItemStatus.failure, RunItemStatus.error}
                ),
                execution_count=0 if status == RunItemStatus.untested else 1,
                last_executed_at=None if status == RunItemStatus.untested else (started if is_open_item else finished),
                started_at=None if status == RunItemStatus.untested else started,
                finished_at=None if status in {RunItemStatus.untested, RunItemStatus.in_progress} else finished,
                duration_ms=None if status in {RunItemStatus.untested, RunItemStatus.in_progress} else duration_ms,
                created_at=test_run.created_at,
                updated_at=script_now if is_latest_run and is_open_item else finished,
            )
            db.add(item)
            item_statuses.append(status)
            if status != RunItemStatus.untested and not is_open_item:
                latest_finished_at = max(latest_finished_at, finished)
            bundle.run_cases_count += 1
            bundle.run_rows_count += len(rows)
            _add_run_item_details(db, item, row_statuses, rows, users, started, rng)

        test_run.status = _status_for_run(item_statuses, is_latest_run=is_latest_run)
        if test_run.status == TestRunStatus.completed:
            test_run.completed_at = latest_finished_at
            test_run.updated_at = latest_finished_at
        elif is_latest_run:
            test_run.updated_at = script_now


def _dataset_snapshot(dataset: TestDataset | None, rows: Sequence[DatasetRow]) -> dict[str, Any]:
    if dataset is None:
        return {}
    return {
        "dataset_id": dataset.id,
        "name": dataset.name,
        "revision": dataset.current_revision_number,
        "rows": [{"row_key": row.row_key, "values": row.values_json} for row in rows],
    }


def _actual_result(status: RunItemStatus) -> str:
    match status:
        case RunItemStatus.passed:
            return "The actual result matched the expected result."
        case RunItemStatus.failure:
            return "A business outcome mismatch was found."
        case RunItemStatus.error:
            return "The service returned a technical error."
        case RunItemStatus.blocked:
            return "The scenario is blocked by an external dependency."
        case RunItemStatus.skipped:
            return "The scenario was skipped by release team decision."
        case RunItemStatus.in_progress:
            return "Execution has started and the result is awaiting confirmation."
        case _:
            return "The scenario has not been executed yet."


def _add_run_item_details(
    db: AsyncSession,
    item: RunItem,
    row_statuses: Sequence[RunItemStatus],
    rows: Sequence[DatasetRow],
    users: Sequence[User],
    started_at: datetime,
    rng: random.Random,
) -> None:
    result_statuses = [RunItemStatus.in_progress] if item.status == RunItemStatus.in_progress else [RunItemStatus.in_progress, item.status]
    for sequence, status in enumerate(result_statuses, start=1):
        if item.status == RunItemStatus.untested:
            break
        result_started = started_at + timedelta(minutes=sequence)
        result_finished = None if item.status == RunItemStatus.in_progress else result_started + timedelta(minutes=rng.randint(1, 8))
        db.add(
            RunResult(
                id=generate_id(),
                run_item_id=item.id,
                sequence=sequence,
                status=status,
                time=item.time,
                comment=f"{DEMO_MARKER} Attempt {sequence}.",
                defect_ids=item.defect_ids if sequence == len(result_statuses) else [],
                executed_by=item.executed_by,
                started_at=result_started,
                finished_at=result_finished,
                duration_ms=None if result_finished is None else int((result_finished - result_started).total_seconds() * 1000),
                created_at=result_started,
                updated_at=result_finished or result_started,
            )
        )
    if item.status != RunItemStatus.untested:
        db.add(
            RunCaseHistory(
                id=generate_id(),
                run_case_id=item.id,
                from_status=RunItemStatus.untested.value,
                to_status=item.status.value,
                time=item.time,
                comment=item.comment,
                defect_ids=item.defect_ids,
                actual_result=item.actual_result,
                system_out=item.system_out,
                system_err=item.system_err,
                executed_by_id=item.executed_by,
                started_at=item.started_at,
                finished_at=item.finished_at,
                duration_ms=item.duration_ms,
                changed_by_id=item.executed_by,
                changed_at=item.finished_at or started_at,
            )
        )
    for row_index, (dataset_row, row_status) in enumerate(zip(rows, row_statuses, strict=False), start=1):
        row_started = started_at + timedelta(minutes=row_index)
        row_finished = row_started + timedelta(minutes=rng.randint(1, 5))
        row_is_untested = row_status == RunItemStatus.untested
        db.add(
            RunCaseRow(
                id=generate_id(),
                run_case_id=item.id,
                row_order=row_index,
                scenario_label=dataset_row.scenario_label or dataset_row.row_key,
                row_snapshot=dataset_row.values_json,
                comment=f"{DEMO_MARKER} Dataset row execution.",
                defect_ids=[f"BUG-{rng.randint(1200, 9999)}"] if row_status == RunItemStatus.failure else [],
                actual_result=_actual_result(row_status),
                system_out="Generated row output",
                system_err="Row-level assertion failed" if row_status == RunItemStatus.failure else None,
                executed_by=None if row_is_untested else users[row_index % len(users)].id,
                status=row_status,
                execution_count=0 if row_is_untested else 1,
                last_executed_at=None if row_is_untested else row_finished,
                started_at=None if row_is_untested else row_started,
                finished_at=None if row_is_untested else row_finished,
                duration_ms=None if row_is_untested else int((row_finished - row_started).total_seconds() * 1000),
                created_at=item.created_at,
                updated_at=item.created_at if row_is_untested else row_finished,
            )
        )


