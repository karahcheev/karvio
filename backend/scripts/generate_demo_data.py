            service=str(profile["service"]),
            env=bundle.environments[run_index % len(bundle.environments)].name,
            scenario=str(profile["scenario"]),
            load_profile=str(profile["load_profile"]),
            branch="release/2.4" if period_index >= periods_per_profile - 3 else "main",
            commit=hashlib.sha1(f"{config.slug}-{bundle.project.id}-{run_index}".encode()).hexdigest()[:12],
            build=f"perf-{config.slug}-{load_kind}-{period_index + 1:02d}",
