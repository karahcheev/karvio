            baseline_policy="tagged" if baseline_ref is None else "manual",
            baseline_label=f"{profile['name']} baseline",
            summary=summary,
            regressions=regressions,
            metrics_comparison=metrics_comparison,
            environment_snapshot=_perf_environment_snapshot(
                bundle=bundle,
                run_index=run_index,
                profile=profile,
                summary=summary,
                rng=rng,
            ),
            acknowledged=verdict == "red" and period_index % 2 == 0,
            created_by=users[2].id,
