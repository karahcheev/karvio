        if baseline_ref is None:
            run.baseline_ref = run.id
            baseline_run_by_kind[load_kind] = run.id
            baseline_summary_by_kind[load_kind] = summary

        tx_metrics = _perf_transaction_metrics(profile, summary, period_index, periods_per_profile, rng)
        baseline_tx_metrics = baseline_transactions_by_kind.get(load_kind)
