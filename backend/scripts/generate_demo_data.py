def _add_products_and_components(
    db: AsyncSession,
    bundle: ProjectBundle,
    template: dict[str, Any],
    users: Sequence[User],
    rng: random.Random,
) -> None:
    for idx, (name, key, description) in enumerate(template["products"], start=1):
        product = Product(
            id=generate_id(),
            project_id=bundle.project.id,
            name=name,
            key=f"{key}-{bundle.project.id[-1]}",
            description=f"{DEMO_MARKER} {description}.",
            owner_id=users[idx % len(users)].id,
            status=ProductStatus.active,
            tags=["release", "customer-facing"] if idx == 1 else ["internal", "release-scope"],
            created_at=_dt_offset(-42),
            updated_at=_dt_offset(-2),
        )
        db.add(product)
        bundle.products.append(product)

    component_specs = COMPONENT_BLUEPRINTS.get(str(template["name"]), COMPONENT_BLUEPRINTS["Karvio Commerce"])
    component_by_key: dict[str, Component] = {}
    for idx, spec in enumerate(component_specs, start=1):
        (
            business_criticality,
            change_frequency,
            integration_complexity,
            defect_density,
            production_incident_score,
            automation_confidence,
        ) = spec["risk"]
        component = Component(
            id=generate_id(),
            project_id=bundle.project.id,
            name=str(spec["name"]),
            key=f"{spec['key']}-{bundle.project.id[-1]}",
            description=f"{DEMO_MARKER} {spec['description']}.",
            owner_id=users[(idx + 1) % len(users)].id,
            status=ProductStatus.active,
            tags=list(dict.fromkeys([*spec["tags"], *rng.sample(TAGS, k=1)])),
            business_criticality=business_criticality,
            change_frequency=change_frequency,
            integration_complexity=integration_complexity,
            defect_density=defect_density,
            production_incident_score=production_incident_score,
            automation_confidence=automation_confidence,
            created_at=_dt_offset(-41 + (idx % 5)),
            updated_at=_dt_offset(-(idx % 7)),
        )
        component.risk_score, component.risk_level = compute_risk_score_and_level(component)
        db.add(component)
        bundle.components.append(component)
        component_by_key[str(spec["key"])] = component

    product_link_order: dict[str, int] = {product.id: 0 for product in bundle.products}
    for spec in component_specs:
        component = component_by_key[str(spec["key"])]
        for product_index in spec["products"]:
            if product_index >= len(bundle.products):
                continue
            product = bundle.products[product_index]
            product_link_order[product.id] += 1
            is_core = component.risk_level in {ComponentRiskLevel.high, ComponentRiskLevel.critical} or product_link_order[product.id] <= 3
            bundle.product_components_by_product.setdefault(product.id, []).append(component)
            db.add(
                ProductComponentLink(
                    id=generate_id(),
                    product_id=product.id,
                    component_id=component.id,
                    is_core=is_core,
                    sort_order=product_link_order[product.id],
                )
            )

    seen_dependencies: set[tuple[str, str]] = set()
    for spec in component_specs:
        source = component_by_key[str(spec["key"])]
        for target_key in spec["depends_on"]:
            target = component_by_key.get(str(target_key))
            if target is None or source.id == target.id:
                continue
            edge_key = (source.id, target.id)
            if edge_key in seen_dependencies:
                continue
            seen_dependencies.add(edge_key)
            db.add(
                ComponentDependency(
                    id=generate_id(),
                    source_component_id=source.id,
                    target_component_id=target.id,
                    dependency_type=ComponentDependencyType.depends_on,
                    created_at=_dt_offset(-20 + (len(seen_dependencies) % 6)),
                )
            )


